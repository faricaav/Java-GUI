    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int selectedRow = jTable2.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diupdate!", "Validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String no = jTable2.getValueAt(selectedRow, 0).toString();
        String nama = jTextField1.getText();
        String NIK = jTextField2.getText();
        Date selectedDate = jDateChooser1.getDate();
        String alamat = jTextArea1.getText();
        String jenis_kelamin = jComboBox1.getSelectedItem().toString();

        if (nama.isEmpty() || NIK.isEmpty() || selectedDate == null || alamat.isEmpty() || jenis_kelamin.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!", "Validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String formattedDate = dateFormat.format(selectedDate);

            // Cek apakah NIK sudah ada dalam database
            String cekSql = "SELECT COUNT(*) FROM pasien WHERE NIK = ? AND no != ?";
            PreparedStatement cekSt = con.prepareStatement(cekSql);
            cekSt.setString(1, NIK);
            cekSt.setString(2, no);
            ResultSet resultSet = cekSt.executeQuery();
            resultSet.next();
            int nikCount = resultSet.getInt(1);

            if (nikCount > 0) {
                JOptionPane.showMessageDialog(this, "NIK sudah ada dalam database!", "Validasi", JOptionPane.ERROR_MESSAGE);
                return; 
            }

            String sql = "UPDATE pasien SET nama=?, NIK=?, tgl_lahir=?, jenis_kelamin=?, alamat=? WHERE no=?";
            PreparedStatement st = con.prepareStatement(sql);

            st.setString(1, nama);
            st.setString(2, NIK);
            st.setString(3, formattedDate);
            st.setString(4, jenis_kelamin);
            st.setString(5, alamat);
            st.setString(6, no);

            int rowUpdated = st.executeUpdate();
            if (rowUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
                resetform();
                getData(currentPage);
            } else {
                JOptionPane.showMessageDialog(this, "Gagal update data.", "Error", JOptionPane.ERROR_MESSAGE);
            }
            st.close();
        } catch (SQLException e) {
            e.printStackTrace(); // Menampilkan pesan kesalahan ke konsol
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan dalam menjalankan query.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int selectedRow = jTable2.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!", "Validasi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin data ini mau dihapus?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {

            try {
                String sql = "DELETE FROM pasien WHERE no = ?";
                PreparedStatement st = con.prepareStatement(sql);

                // Ambil nilai 'no' dari baris yang dipilih
                String no = jTable2.getValueAt(selectedRow, 0).toString(); 

                st.setString(1, no);

                int rowDelete = st.executeUpdate();
                if (rowDelete > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                    getData(currentPage);
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan dalam menjalankan query.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton5ActionPerformed

