    private void getData(int page) {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        int limit = 9;
        int offset = (page - 1) * limit;

        try {
            String sql = "SELECT * FROM pasien LIMIT ? OFFSET ?";
            PreparedStatement st = con.prepareStatement(sql);
            st.setInt(1, limit);
            st.setInt(2, (page - 1) * limit);
            ResultSet rs = st.executeQuery();

            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MMM-dd");

            while (rs.next()) {
                int no = rs.getInt("No");
                long NIK = rs.getLong("NIK");
                String nama = rs.getString("nama");
                java.sql.Date sqlDate = rs.getDate("tgl_lahir");
                String tanggal = dateFormat.format(sqlDate);
                String jk = rs.getString("jenis_kelamin");
                String alamat = rs.getString("alamat");

                Object[] rowData = {no, NIK, nama, tanggal, jk, alamat};
                model.addRow(rowData);
            }
            rs.close();
            st.close();

            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(DefaultTableCellRenderer.CENTER);
            for (int i = 0; i < jTable2.getColumnCount(); i++) {
                jTable2.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
            }

            jTable2.getColumnModel().getColumn(0).setPreferredWidth(1);
            jTable2.getColumnModel().getColumn(1).setPreferredWidth(20);
            jTable2.getColumnModel().getColumn(2).setPreferredWidth(20);
            jTable2.getColumnModel().getColumn(3).setPreferredWidth(20);
            jTable2.getColumnModel().getColumn(4).setPreferredWidth(20);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan dalam menjalankan query.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

